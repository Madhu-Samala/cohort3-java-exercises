package com.qa.java.oop.interfaces;

public class CarProperties {
	
	int id;
	String name;
	double price;
	int engineCC;
	String fuelType;
	int manufacturingYear;
	
	
	
	public CarProperties(int id, String name, double price, int engineCC, String fuelType, int manufacturingYear) {
		this.id = id;
		this.name = name;
		this.price = price;
		this.engineCC = engineCC;
		this.fuelType = fuelType;
		this.manufacturingYear = manufacturingYear;
	}
	
	

	

}
